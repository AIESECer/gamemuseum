<h2>L'emulation:</h2>
<p>Cest le principe de faire fonctionner les jeux consoles (PlayStation, SNES, MegaDrive etc..)sur PC. L'emulation a besoin de 2 fichiers pour fonctionner qui sont:</p>
<ul>
  <li>L'emulateur: l'emulateur est un programme qui fait fonction de console, ils permet de lire les room(jeux).Contrairement a la room dont les droits de possession sont limit&eacute;s, l'emulateur est  gratuit et parfaitement legal. Les emulateurs sont aussi utilis&eacute;s dans les domaines de la creation de jeux, ils servent a faire les test sur PC avant de le mettre sur console.</li>
  <li>La room: c'est le ou les fichiers suivant les consoles et les emulateurs qui font office de jeux et qui sont lus par les emulateurs. Les rooms ont des conditions de possessions tres limit&eacute;s contrairement aux emulateurs, elles sont gratuites mais leur possession est interdite si vous ne possedez pas le jeux original.</li>
</ul>
<p>Note: l'emulation n'existe pas que dans les jeux video. Il existe aussi de l'emulation pour faire fonctionner des logiciels d'autres machines et OS(Mac,Linux, Solaris) sur PC.</p>
<h2>L'histoire: </h2>
<p>Le principe de l'emulation a ete invente par l'Italien Nicoals Salmoria. Son but etait de faire fonctionner de vieux jeux d'arcades impossible a trouver. Lui et ses amis trouverent de vielles machines d'arcades des ann&eacute;es 80 &quot;dans de vieux bars&quot; . Au debut des ann&eacute;es 90 il faisait fonctionner ses jeux sur le 1er emulateur console (qu'il avait cre&eacute;) du nom de MAME don les initiales signifient: &quot;Multiple Arcade Machine Emulator&quot;. Le princpe qu'il inventa passa vite ax jeux recents.De nos jours ont trouve des jeux qui n'ont pas 2 mois d'existence disponibles en telechargement sur le net. L'emulation tel qu'on la connait de nos jours n'existe que depuis 1997 car avant cette date il fallait programmer chaque emulateur pour chaque ordinateur, car le fonctionnement n'etait pas le meme qu'au jourd'hui. L'arriv&eacute;e d'Internet dans les foyers provoque la diffusion des &eacute;mulateurs et des   roms dans le monde entier.</p>
